using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Graphics_editor
{
    public class FPoint
    {
        public float X;
        public float Y;

        public FPoint()
        {
            this.X = 0;
            this.Y = 0;
        }

        public FPoint(float X, float Y)
        {
            this.X = X;
            this.Y = Y;
        }
    }
}
